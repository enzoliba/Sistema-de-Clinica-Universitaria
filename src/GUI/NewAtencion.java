    private void comboCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCitaActionPerformed
        try{
            if(comboCita.getSelectedItem() != null){
                
                String item = comboCita.getSelectedItem().toString();
                String idCita = item.split("-") [0];
                
                Connection cn = Conexion.getConnect().getConexion(); 
                String sql = "SELECT C.idCita, DATE(C.fechaHora) AS fecha, TIME(C.fechaHora) AS hora, C.motivo, C.estado," 
                        + " P.dni AS dniPaciente, "
                        + "CONCAT (M.nombre, ' ', M.apellidoPaterno, '', M.apellidoMaterno) AS medico, "
                        + "A.nombre AS consultorio "
                        + "FROM CITA C "
                        + "INNER JOIN PACIENTE PA ON C.idPaciente = PA.idPersona "
                        + "INNER JOIN PERSONA P ON PA.idPersona = P.idPersona "
                        + "INNER JOIN EMPLEADO E ON C.idEmpleado = E.idPersona "
                        + "INNER JOIN PERSONA M ON E.idPersona = M.idPersona "
                        + "INNER JOIN AMBIENTE A ON C.idAmbiente = A.idAmbiente "
                        + "WHERE C.IdCita = ?"; 
                PreparedStatement pst = cn.prepareStatement(sql); 
                pst.setString(1, idCita);
                ResultSet rs = pst.executeQuery(); 
                
                if(rs.next()){
                    labelCodigo.setText(rs.getString("idCita"));
                    labelDni.setText(rs.getString("DniPaciente"));
                    labelMedico.setText(rs.getString("Medico"));
                    labelAmbiente.setText(rs.getString ("Consultorio"));
                    labelFechaCita.setText(rs.getString ("Fecha"));
                    labelHoraCita.setText(rs.getString ("Hora"));
                    labelMotivoCita.setText(rs.getString ("Motivo"));
                    jLabel16.setText(rs.getString ("Estado"));

               
                }
                rs.close();
                pst.close();
                cn.close();
                
            }
          } catch (Exception e){
                JOptionPane.showMessageDialog(this, "ERROR al cargar citas: " + e.getMessage());    
        }
    }//GEN-LAST:event_comboCitaActionPerformed

