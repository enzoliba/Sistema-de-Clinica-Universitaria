    private void cargarCitas() {
      try{
          Connection cn = Conexion.getConnect().getConexion(); 
          String sql = "SELECT idCita, motivo FROM CITA"; 
          PreparedStatement ps = cn.prepareStatement(sql); 
          ResultSet rs = ps.executeQuery(); 
          
          comboCita.removeAllItems();
          while(rs.next()){
              comboCita.addItem(rs.getString("idCita")+ "-" + rs.getString("motivo"));
          }
          rs.close();
          ps.close();
          cn.close();
      } catch (Exception e){
          JOptionPane.showMessageDialog(this, "ERROR al cargar citas: " + e.getMessage());
      }
    }

