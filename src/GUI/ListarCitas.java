    private void cargarCitas(){
        DCita pacDao = new DCita();
        ArrayList<ECita> lista;
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat formato2 = new SimpleDateFormat("HH:mm");
        try{
            lista = pacDao.listarCitas();
            for( ECita obj : lista ){
                String fecha = formato.format(obj.getFecha());
                String hora = formato2.format(obj.getHora());
                String fila[] = {String.valueOf(obj.getIdCita()),
                                fecha,
                                hora,
                                obj.getMotivo(),
                                obj.getEstado(),
                                String.valueOf(obj.getPaciente().getIdPersona()),
                                String.valueOf(obj.getEmpleado().getIdPersona()),
                                String.valueOf(obj.getAmbiente().getIdAmbiente())
                                };
                this.modelo.addRow(fila);
            }
        }catch( Exception e ){
            
        }
    }
    
