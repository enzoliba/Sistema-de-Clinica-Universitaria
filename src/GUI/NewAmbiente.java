    private void BtnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegisterActionPerformed
        //VALIDACIONES PARA LOS DATOS
        if(this.TxtNombreAmbi.getText().length() == 0){
           JOptionPane.showMessageDialog(rootPane, "Ingrese el nombre del Ambiente");
           return; 
        }
        if((int) this.jSpinnerCapacidad.getValue() <= 0){
            JOptionPane.showMessageDialog(rootPane, "La capacidad debe ser mayor a 0");
           return; 
        }
        /*if (!this.ChkAmbienteDispo.isSelected()) {
        JOptionPane.showMessageDialog(rootPane, "El ambiente debe estar disponible al registrarse");
        return;
        }*/
        
        EAmbiente amb = new EAmbiente();
        
        amb.setIdAmbiente(codeGen);
        amb.setNombre(this.TxtNombreAmbi.getText());
        amb.setCapacidad((int)this.jSpinnerCapacidad.getValue());
        //DISPONIBILIDAD AMBIENTE
        amb.setDisponibilidad(this.ChkAmbienteDispo.isSelected());
        
        String tipo[] = {"",""};
        DAmbiente ambDao = new DAmbiente();
        int opc = 1;
        if (this.BtnRegister.getText().equals("Registrar")) {
            opc = confirmacionRegistroModificar(amb, "Registro");
            tipo[0] = "registrar";
            tipo[1] = "registrado";
        }else if (this.BtnRegister.getText().equals("Actualizar")){
            opc = confirmacionRegistroModificar(amb, "Actualizacion");
            tipo[0] = "actualizar";
            tipo[1] = "actualizado";
            amb.setIdAmbiente(this.codigoAmbiente.getText());
        }
        
         if (opc != 0) {
            return;
        }
         
         try {
            if(this.BtnRegister.getText().equals("Registrar")){
                ambDao.registrarAmbiente(amb);
            
            }else if(this.BtnRegister.getText().equals("Actualizar")){
                ambDao.actualizarAmbiente(amb);
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewAmbiente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "El Ambiente no se pudo registrar, revise que los datos sean correctos.");
            return;
        }
         JOptionPane.showMessageDialog(rootPane, "Ambiente registrado correctamente ");
         try {
            limpiar();
        } catch (SQLException ex) {
            Logger.getLogger(NewPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnRegisterActionPerformed
    
    public int confirmacionRegistroModificar(EAmbiente amb, String confir){
        int opcion = JOptionPane.showConfirmDialog(
            rootPane,
            "<html><b>Datos del Paciente:</b><br>" +
            "Nombre: " + amb.getNombre() + "<br>" +
            "Apellidos: " + amb.getCapacidad(),
            "Verificaci√≥n del Registro",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.INFORMATION_MESSAGE
        );
        return opcion;
    }
    
    public void limpiar() throws SQLException{
        this.TxtNombreAmbi.setText(null);
        this.jSpinnerCapacidad.setValue(0);
        this.codeGen = GeneradorCode.CodeGenerador("AMB", null);
        this.codigoAmbiente.setText(codeGen);
    }
    
