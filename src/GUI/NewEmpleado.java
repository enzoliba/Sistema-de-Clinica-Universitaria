    private void cargarEspecialidades(){
        this.ComboxEspecialidadEmp.removeAllItems();
        this.ComboxEspecialidadEmp.addItem("Seleccionar");
        List<E_Especialidad> listaEsp = DEspecialidad.listarEspecialidades();
        for( E_Especialidad obj : listaEsp ){
            this.ComboxEspecialidadEmp.addItem(obj.getNombre());
        }
        this.ComboxEspecialidadEmp.addItem("No Tiene");
    }
    
    private void cargarRoles() {
        this.ComboxRolEmp.removeAllItems();
        this.ComboxRolEmp.addItem("Seleccionar");
        // Recorrer el enum Rol para añadir las opciones
        for (Rol rol : Rol.values()) {
            if (rol.getDbValue().equalsIgnoreCase("Medico") || rol.getDbValue().equalsIgnoreCase("Enfermero") || rol.getDbValue().equalsIgnoreCase("Seguridad") || rol.getDbValue().equalsIgnoreCase("Limpieza")) { 
                this.ComboxRolEmp.addItem(rol.getDbValue());
            }
        }
    }

