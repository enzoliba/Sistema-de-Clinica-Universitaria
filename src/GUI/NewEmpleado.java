    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        try {
            // Validar campos obligatorios
            if (!validarCampos()) {
                return;
            }

            // Construir el objeto empleado usando el patrón Builder
            E_Empleado nuevoEmpleado = construirEmpleado();

            DEmpleado empDao = new DEmpleado();
            empDao.registrarEmpleadoCompleto(nuevoEmpleado);

            javax.swing.JOptionPane.showMessageDialog(this, 
                "Empleado registrado exitosamente!\nCódigo: " + nuevoEmpleado.getIdPersona(), 
                "Registro Exitoso", 
                javax.swing.JOptionPane.INFORMATION_MESSAGE);

            // Limpiar formulario después del registro exitoso
            limpiarFormulario();

        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error al registrar empleado: " + ex.getMessage(), 
                "Error", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(NewEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnRegistrarActionPerformed

    private E_Empleado construirEmpleado() {
        // Obtener datos del formulario
        String nombres = TxtNombres.getText().trim();
        String apellidoPaterno = TxtApellidoPa.getText().trim();
        String apellidoMaterno = TxtApellidoMa.getText().trim();
        String dni = TxtDni.getText().trim();
        String telefono = TxtTelefono.getText().trim();
        String email = TxtEmail1.getText().trim();
        String direccion = TxtEmail.getText().trim();
        String genero = this.TxtGenero.getText().trim();

        // Obtener rol seleccionado
        String rolSeleccionado = (String) ComboxRolEmp.getSelectedItem();
        Rol rol = obtenerRolPorNombre(rolSeleccionado);

        // Obtener especialidad (solo si es médico)
        E_Especialidad especialidad = null;
        if (rolSeleccionado.equals("Medico")) {
            String especialidadNombre = (String) ComboxEspecialidadEmp.getSelectedItem();
            if (!especialidadNombre.equals("Seleccionar") && !especialidadNombre.equals("No Tiene")) {
                especialidad = obtenerEspecialidadPorNombre(especialidadNombre);
            }
        }

        // Obtener fecha de nacimiento
        java.util.Date fechaNacimiento = jDateChooser1.getDate();

        // Obtener estados de los checkboxes
        boolean disponible = CbDisponible.isSelected();
        boolean activo = CbActivo.isSelected();

        // Fecha de ingreso (fecha actual)
        java.util.Date fechaIngreso = new java.util.Date();

        // Construir el empleado usando el patrón Builder
        E_Empleado empleado = E_Empleado.builder()
            .setIdPersona(codeGenerado)
            .setNombre(nombres)
            .setApellidoPaterno(apellidoPaterno)
            .setApellidoMaterno(apellidoMaterno)
            .setDni(dni)
            .setTelefono(telefono)
            .setCorreo(email)
            .setDireccion(direccion)
            .setFechaNacimiento(fechaNacimiento)
            .setRol(rol)
            .setDisponibilidad(disponible)
            .setEstado(activo)
            .setFechaIngreso(fechaIngreso)
            .setEspecialidad(especialidad)
            .setHorarios(emp.getHorarios())
            .setGenero(genero)
            .build();

        return empleado;
    }
    
    private boolean validarCampos() {
        StringBuilder errores = new StringBuilder();

        // Validar código generado
        if (codeGenerado == null || codeGenerado.isEmpty()) {
            errores.append("- Debe seleccionar un rol para generar el código\n");
        }

        // Validar campos de texto obligatorios
        if (TxtNombres.getText().trim().isEmpty()) {
            errores.append("- El nombre es obligatorio\n");
        }

        if (TxtApellidoPa.getText().trim().isEmpty()) {
            errores.append("- El apellido paterno es obligatorio\n");
        }

        if (TxtApellidoMa.getText().trim().isEmpty()) {
            errores.append("- El apellido materno es obligatorio\n");
        }

        if (TxtDni.getText().trim().isEmpty()) {
            errores.append("- El DNI es obligatorio\n");
        } else if (!validarDNI(TxtDni.getText().trim())) {
            errores.append("- El DNI debe tener 8 dígitos\n");
        }

        if (TxtTelefono.getText().trim().isEmpty()) {
            errores.append("- El teléfono es obligatorio\n");
        }

        if (TxtEmail1.getText().trim().isEmpty()) {
            errores.append("- El email es obligatorio\n");
        } else if (!validarEmail(TxtEmail1.getText().trim())) {
            errores.append("- El formato del email no es válido\n");
        }

        if (TxtEmail.getText().trim().isEmpty()) {
            errores.append("- La dirección es obligatoria\n");
        }

        // Validar fecha de nacimiento
        if (jDateChooser1.getDate() == null) {
            errores.append("- La fecha de nacimiento es obligatoria\n");
        }

        // Validar rol
        String rolSeleccionado = (String) ComboxRolEmp.getSelectedItem();
        if (rolSeleccionado == null || rolSeleccionado.equals("Seleccionar")) {
            errores.append("- Debe seleccionar un rol\n");
        }

        // Validar especialidad para médicos
        if ("Medico".equals(rolSeleccionado)) {
            String especialidadSeleccionada = (String) ComboxEspecialidadEmp.getSelectedItem();
            if (especialidadSeleccionada == null || especialidadSeleccionada.equals("Seleccionar")) {
                errores.append("- Debe seleccionar una especialidad para el médico\n");
            }
        }

        // Mostrar errores si existen
        if (errores.length() > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Por favor corrija los siguientes errores:\n\n" + errores.toString(), 
                "Errores de validación", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }

    private boolean validarDNI(String dni) {
        return dni.matches("\\d{8}");
    }

    private boolean validarEmail(String email) {
        return email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$");
    }

    private Rol obtenerRolPorNombre(String nombreRol) {
        for (Rol rol : Rol.values()) {
            if (rol.getDbValue().equalsIgnoreCase(nombreRol)) {
                return rol;
            }
        }
        return null; // o lanzar una excepción
    }

    private E_Especialidad obtenerEspecialidadPorNombre(String nombreEspecialidad) {
        // Aquí deberías buscar la especialidad en tu base de datos o lista
        // Por ejemplo, usando tu clase DEspecialidad:
        List<E_Especialidad> especialidades = DEspecialidad.listarEspecialidades();
        for (E_Especialidad esp : especialidades) {
            if (esp.getNombre().equals(nombreEspecialidad)) {
                return esp;
            }
        }
        return null;
    }

