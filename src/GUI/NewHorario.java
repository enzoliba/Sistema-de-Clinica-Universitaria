    private void BtnAgregarHorarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarHorarioActionPerformed
        EHorario horario = null;
        try {
            // Validaciones de campos vacíos o nulos
            if (this.ComboxDia.getSelectedItem() == null || 
                this.ComboxDia.getSelectedItem().toString().trim().isEmpty() ||
                this.ComboxDia.getSelectedIndex() == 0) { // Asumiendo que index 0 es "Seleccionar"
                JOptionPane.showMessageDialog(this, 
                    "Por favor seleccione un día de la semana.", 
                    "Campo requerido", 
                    JOptionPane.WARNING_MESSAGE);
                this.ComboxDia.requestFocus();
                return;
            }

            // Validación correcta para JTimeChooser
            if (this.entradaTime.getTimeField().getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "Por favor ingrese la hora de entrada.", 
                    "Campo requerido", 
                    JOptionPane.WARNING_MESSAGE);
                this.entradaTime.requestFocus();
                return;
            }

            if (this.salidaTime.getTimeField().getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "Por favor ingrese la hora de salida.", 
                    "Campo requerido", 
                    JOptionPane.WARNING_MESSAGE);
                this.salidaTime.requestFocus();
                return;
            }

            // Obtener valores de los campos
            String diaSeleccionado = (String) this.ComboxDia.getSelectedItem();
            String horaEntradaStr = this.entradaTime.getFormatedTime();
            String horaSalidaStr = this.salidaTime.getFormatedTime();

            // Validación adicional de formato
            if (horaEntradaStr == null || horaEntradaStr.trim().isEmpty() || horaEntradaStr.equals("00:00")) {
                JOptionPane.showMessageDialog(this, 
                    "Por favor ingrese una hora de entrada válida.", 
                    "Campo requerido", 
                    JOptionPane.WARNING_MESSAGE);
                this.entradaTime.requestFocus();
                return;
            }

            if (horaSalidaStr == null || horaSalidaStr.trim().isEmpty() || horaSalidaStr.equals("00:00")) {
                JOptionPane.showMessageDialog(this, 
                    "Por favor ingrese una hora de salida válida.", 
                    "Campo requerido", 
                    JOptionPane.WARNING_MESSAGE);
                this.salidaTime.requestFocus();
                return;
            }

            // Convertir strings de tiempo a LocalTime para comparación
            LocalTime horaEntrada = LocalTime.parse(horaEntradaStr, DateTimeFormatter.ofPattern("HH:mm"));
            LocalTime horaSalida = LocalTime.parse(horaSalidaStr, DateTimeFormatter.ofPattern("HH:mm"));

            // Validación: verificar si ya existe un horario para ese día
            DiaSemana diaEnum = DiaSemana.fromDbValue(diaSeleccionado);
            boolean diaYaExiste = listaHorarios.stream()
                    .anyMatch((EHorario horario1) -> horario1.getDia().equals(diaEnum));

            if (diaYaExiste) {
                int respuesta = JOptionPane.showConfirmDialog(this, 
                    "Ya existe un horario para " + diaSeleccionado + ". ¿Desea reemplazarlo?", 
                    "Día duplicado", 
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);

                if (respuesta == JOptionPane.NO_OPTION) {
                    return;
                } else {
                    // Eliminar el horario existente para ese día
                    listaHorarios.removeIf((EHorario horario1) -> horario1.getDia().equals(diaEnum));
                    // También eliminar de la tabla
                    for (int i = 0; i < this.modelo.getRowCount(); i++) {
                        if (this.modelo.getValueAt(i, 0).equals(diaSeleccionado)) {
                            this.modelo.removeRow(i);
                            break;
                        }
                    }
                }
            }

            // Crear nuevo objeto EHorario
            horario = new EHorario(
                null,
                diaEnum,
                horaEntrada,
                horaSalida
            );

            // Agregar a la lista PRIMERO (para mantener consistencia)
            listaHorarios.add(horario);

            // Agregar fila a la tabla
            String fila[] = {
                diaSeleccionado,
                horaEntradaStr,
                horaSalidaStr
            };
            this.modelo.addRow(fila);

            // Opcional: ordenar la tabla por día de la semana
            ordenarTablaPorDia();

            // Limpiar campos después de agregar exitosamente
            this.ComboxDia.setSelectedIndex(0);

            // Mensaje de confirmación
            JOptionPane.showMessageDialog(this, 
                "Horario agregado correctamente.", 
                "Éxito", 
                JOptionPane.INFORMATION_MESSAGE);

        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, 
                "Error en el formato de hora. Por favor verifique que las horas estén en formato HH:mm", 
                "Error de formato", 
                JOptionPane.ERROR_MESSAGE);
            // Si hay error de parsing, no agregamos nada
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al agregar el horario: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            // Si el horario se creó pero hubo error después, lo removemos de la lista
            if (horario != null && listaHorarios.contains(horario)) {
                listaHorarios.remove(horario);
            }
            return;
        }
    }//GEN-LAST:event_BtnAgregarHorarioActionPerformed

