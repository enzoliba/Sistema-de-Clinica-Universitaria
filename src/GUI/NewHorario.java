    private void actualizarTabla( List<EHorario> listaHorarios ){
        if (listaHorarios != null ||  !listaHorarios.isEmpty()) {
            for( EHorario obj : listaHorarios ){
                // Agregar fila a la tabla
                String fila[] = {
                    obj.getDia().getDbValue(),
                    obj.getHoraEntrada().toString(),
                    obj.getHoraSalida().toString()
                };
                this.modelo.addRow(fila);
            }
        }
    }
    
   private void ordenarTablaPorDia() {
    try {
        // Crear un array con el orden deseado de los días
        String[] ordenDias = {"Lunes", "Martes", "Miercoles", "Jueves", "Viernes", "Sabado", "Domingo"};

        // Obtener datos actuales de la tabla
        java.util.List<String[]> filas = new java.util.ArrayList<>();
        for (int i = 0; i < this.modelo.getRowCount(); i++) {
            String[] fila = new String[3];
            fila[0] = (String) this.modelo.getValueAt(i, 0); // Día
            fila[1] = (String) this.modelo.getValueAt(i, 1); // Entrada
            fila[2] = (String) this.modelo.getValueAt(i, 2); // Salida
            filas.add(fila);
        }

        // Ordenar según el orden de días establecido
        filas.sort((f1, f2) -> {
            int index1 = java.util.Arrays.asList(ordenDias).indexOf(f1[0]);
            int index2 = java.util.Arrays.asList(ordenDias).indexOf(f2[0]);
            return Integer.compare(index1, index2);
        });

        // Limpiar tabla y agregar filas ordenadas
        this.modelo.setRowCount(0);
        for (String[] fila : filas) {
            this.modelo.addRow(fila);
        }

        // También reordenar la lista de horarios para mantener consistencia
        listaHorarios.sort((h1, h2) -> {
            int index1 = java.util.Arrays.asList(ordenDias).indexOf(h1.getDia().getDbValue());
            int index2 = java.util.Arrays.asList(ordenDias).indexOf(h2.getDia().getDbValue());
            return Integer.compare(index1, index2);
        });

    } catch (Exception e) {
        // Si falla el ordenamiento, no es crítico, solo log
        System.err.println("Error al ordenar tabla: " + e.getMessage());
    }
}
    
