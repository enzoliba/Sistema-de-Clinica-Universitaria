    private void cargarAmbientes(){
        DAmbiente pacDao = new DAmbiente();
        ArrayList<EAmbiente> lista;
        
        try{
            lista = pacDao.listarAmbientes();
            for( EAmbiente obj : lista ){
                String disponible;
                if( obj.isDisponibilidad() == true ){
                    disponible = "Disponible";
                }else{
                    disponible = "No Disponible";
                }
                String fila[] = {String.valueOf(obj.getIdAmbiente()),
                                 obj.getNombre(),
                                 String.valueOf(obj.getCapacidad()),
                                 disponible
                                };
                this.modelo.addRow(fila);
            }
        }catch( Exception e ){
            
        }
    }
    
