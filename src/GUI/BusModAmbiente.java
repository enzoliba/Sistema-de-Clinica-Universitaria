    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        DAmbiente ambDao = new DAmbiente();
        String idAmbiente;
        EAmbiente amb = new EAmbiente();
        if (this.LAmbientes.getSelectedRow() != -1){
            idAmbiente = (String) this.LAmbientes.getValueAt(this.LAmbientes.getSelectedRow(), 0);
            amb = ambDao.buscarAmbientePorId(idAmbiente);
            NewAmbiente np;
            try {
                np = new NewAmbiente(amb);
                np.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(BusModAmbiente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un ambiente", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

